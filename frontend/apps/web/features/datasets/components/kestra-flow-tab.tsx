function YamlViewer({ code }: { code: string }) {
  const lineCount = useMemo(() => code.split("\n").length, [code])
  return (
    <CodeViewer
      code={code}
      language="yaml"
      height={Math.min(lineCount * 20 + 20, 600)}
      copyLabel="Kestra YAML"
    />
  )
}

