function PythonViewer({ code, copyLabel = "Airflow DAG" }: { code: string; copyLabel?: string }) {
  const lineCount = useMemo(() => code.split("\n").length, [code])
  return (
    <CodeViewer
      code={code}
      language="python"
      height={Math.min(lineCount * 20 + 20, 600)}
      copyLabel={copyLabel}
    />
  )
}

